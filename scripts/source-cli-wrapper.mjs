export function renderSourceCliWrapper({ nodePath, cliPath, platform = process.platform }) {
  if (!nodePath || !cliPath) throw new Error("nodePath and cliPath are required.");
  if (platform === "win32") {
    return `@echo off\r\nsetlocal DisableDelayedExpansion\r\n${quoteCmd(nodePath)} ${quoteCmd(cliPath)} %*\r\n`;
  }
  return `#!/bin/sh\n# Generated by the Preflight Scout source installer.\nexec ${quoteShell(nodePath)} ${quoteShell(cliPath)} "$@"\n`;
}

function quoteShell(value) {
  return `'${value.replaceAll("'", "'\\''")}'`;
}

function quoteCmd(value) {
  return `"${value.replaceAll("%", "%%").replaceAll('"', '""')}"`;
}
